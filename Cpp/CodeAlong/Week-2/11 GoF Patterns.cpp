// # Gang-of-Four Patterns
//
// - Patterns from the book
//   "Design Patterns: Elements of Reusable Object-Oriented Software"
// - The 4 authors were jokingly called "Gang of Four"
// - The most famous collection of patterns
// - Published in 1994
// - 23 patterns

// %% [markdown]
//
// ## Properties
//
// - Domain independent
// - Language independent
// - Object oriented
// - Relatively low level

// %% [markdown]
//
// ## Examples:
//
// - **Builder:** Build complex objects step by step
// - **Factory Method:** Create objects without knowing the concrete class
// - **Adapter:** Adapt the interface of a class to another interface
// - **Observer:** Pass changes to an object to other objects
// - **Strategy:** Make algorithms interchangeable
// - **Template Method:** Define the basic structure of an operation, details in
//   subclasses
