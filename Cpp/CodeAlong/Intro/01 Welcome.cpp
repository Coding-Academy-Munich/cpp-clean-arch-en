// # Welcome!
//
// I am pleased to welcome you to the **Modern Embedded C++** course.
//
// In this course, we will discuss many exciting topics related to the
// development of modern C++ programs: testing, design patterns, software
// architecture with SOLID and GRASP, and more.
//
// The course consists of video material, workshops, and live sessions. At the
// beginning of each week, you will receive videos here on Memberspot that cover
// the course content. Many of the videos are accompanied by workshops that you
// should complete independently throughout the week. Here, you can practice and
// deepen the techniques discussed in the videos.
//
// Every Friday, we meet for a two-hour live session to discuss the week's
// content. Here you can ask questions, discuss further topics, and we can do
// live coding together, among other activities.
//
// All course materials can be found in the associated [GitHub
// Repository](https://github.com/Coding-Academy-Munich/embedded-cpp-de/tree/master).
// If you are familiar with Git, the best way to work with the materials is to
// clone the repository. If you are not familiar with Git, you can download the
// repository content as a zip file by either going to the [Repository Home
// Page](https://github.com/Coding-Academy-Munich/embedded-cpp-de/tree/master),
// clicking the green "<> Code" button, and selecting "Download ZIP", or by
// clicking [this
// link](https://github.com/Coding-Academy-Munich/embedded-cpp-de/archive/refs/heads/master.zip).
// However, I will likely update the slides multiple times during the course; to
// receive these updates, you will need to re-download the ZIP archive after
// each update.
//
// We use Jupyter Notebooks for the course slides. These notebooks allow for the
// interactive execution of code directly from the slides. All notebooks for the
// course are included in the course materials. In the intro section, you will
// find videos explaining how to run the notebooks on your computer using a
// Docker image. Each notebook is available in two versions: as completed slides
// containing all code and solutions to all tasks, and as code-alongs, where
// parts of the source code and solutions are missing. I use the code-alongs in
// the videos, and you are welcome to use these notebooks if you want to follow
// along with the live coding parts of the videos (which I highly recommend).
// The notebooks are in the "Notebooks" folder within the course materials, with
// slides and code-alongs in subfolders.
//
// If you only want to view the slides without participating in live coding,
// there is also an HTML version available. This can be found in the `Html`
// folder, also with subfolders `CodeAlong` and `Folien`.
//
// We have an experimental additional way for you to work with the notebooks: at
// [https://jh1.mhoelzl.de/](https://jh1.mhoelzl.de/), we have installed a
// JupyterLab server where you can log in with the name from your email address
// in all lower-case letters. For example, if your email address is
// `Max.Mustermann@example.com`, your login name is `max.mustermann`. During the
// first login, you can set a password for yourself to log in. Then you can use
// [this
// link](https://jh1.mhoelzl.de/hub/user-redirect/git-pull?repo=https%3A%2F%2Fgithub.com%2FCoding-Academy-Munich%2Fembedded-cpp-de&urlpath=lab%2Ftree%2Fembedded-cpp-de%2FNotebooks%2FFolien%2FIntro%2F11+Arbeiten+mit+Notebooks.ipynb&branch=master)
// to copy the GitHub repository content to the server and work with the
// interactive slides. You can also use the same link to update the slides on
// the server if a new version is available.
