// # SOLID: Introduction
//
// - Collection of five principles/guidelines
// - Collected by Robert C. Martin
// - Language and domain independent
// - Goals: Maintainability, extensibility, reusability

// %% [markdown]
//
// - Single Responsibility Principle (SRP)
// - Open/Closed Principle (OCP)
// - Liskov Substitution Principle (LSP)
// - Interface Segregation Principle (ISP)
// - Dependency Inversion Principle (DIP)

// %% [markdown]
//
// ## GRASP: Introduction
//
// - General Responsibility Assignment Software Patterns
// - Collection of principles/guidelines
// - Collected by Craig Larman
// - Language and domain independent
// - Goals: Maintainability, extensibility, reusability

// %% [markdown]
//
// - GRASP patterns are tools to understand responsibility-driven design
// - 9 patterns
// - Hierarchy of abstractions
// - Some patterns give concrete hints which objects take which responsibilities
// - Others are very abstract principles

